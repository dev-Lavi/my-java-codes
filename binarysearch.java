public class binarysearch {
    public static void main(String[] args) {
        int[] arr = {1,4,6,7,8,23,54,65,76,};
        binaryseacrh(arr);
    }

    static int[] binaryseacrh(int[] arr){
        int left=arr[0];
        int n = arr.length;
        int right=arr[n];
        int mid;
        while(left>=right){
            mid = (left + right)/2;
        }
        return arr;
    }
}
